#
#   Action object
#

##################################################################################################################################
#
#   IMPORTS
#
##################################################################################################################################

import copy

##################################################################################################################################
#
#   CLASS
#
##################################################################################################################################


class ACTION:

    ##################################################################################################################################
    #
    #   VARIBLES
    #
    ##################################################################################################################################

    _logic = None

    _condition = None

    _ID = None

    _total_number_of_actions = None

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    # Constructor
    def __init__(self, logic, condition):
        self.setCondition(condition)
        self.setLogic(logic)

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getLogic(self):
        return self._logic

    # ----------------------------------------------------------------

    def getLabel(self):
        return self.getLogic().getLabel() + self.getConditionLabel()

    # ----------------------------------------------------------------

    def getCopy(self):
        return copy.deepcopy(self)

    # ----------------------------------------------------------------

    def getCondition(self):
        return self._condition

    # ----------------------------------------------------------------

    def getConditionLabel(self):
        return self._condition.getLabel() + self.getLogic().getExtraConditionLabel()

    # ----------------------------------------------------------------

    def getReward(self):
        return self.getLogic().getReward() + self.getCondition().getReward()

    # ----------------------------------------------------------------

    def getName(self):
        return self.getLogic().getName()

    # ----------------------------------------------------------------

    def getType(self):
        return self.getLogic().getType()

    # ----------------------------------------------------------------

    def getID(self):
        return self._ID

    # ----------------------------------------------------------------

    def getJsonAction(self):
        return {"logic": self._logic.getJsonFormat(), "condition": self._condition.getJsonFormat()}

    # ----------------------------------------------------------------

    def isACTION(self):
        return True

    # ----------------------------------------------------------------

    def isDELIVERAction(self):
        return self._logic.isDELIVER()

    # ----------------------------------------------------------------

    def isSENDAction(self):
        return self._logic.isSEND()

    # ----------------------------------------------------------------

    def isSTOPAction(self):
        return self._logic.isSTOP()

    # ----------------------------------------------------------------

    def isDECIDEAction(self):
        return self._logic.isDECIDE()

    # ----------------------------------------------------------------

    def isPROPOSEAction(self):
        return self._logic.isPROPOSE()

    # ----------------------------------------------------------------

    def isFINISHAction(self):
        return self._logic.isFINISH()

    # ----------------------------------------------------------------

    def isSET_ESTIMATIVEAction(self):
        return self._logic.isSET_ESTIMATE()

    # ----------------------------------------------------------------

    def __eq__(self, action):
        return issubclass(type(action), ACTION) and self.getLogic() == action.getLogic() and self.getCondition() == action.getCondition()

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    def setLogic(self, logic):
        self._logic = logic

    # ----------------------------------------------------------------

    def setCondition(self, condition):
        self._condition = condition

    # ----------------------------------------------------------------

    def setID(self, ID):
        self._ID = ID

    # ----------------------------------------------------------------

    def setTotalNumberOfActions(self, value):
        self._total_number_of_actions = value
