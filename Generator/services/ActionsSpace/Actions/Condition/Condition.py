class CONDITION():

    ##################################################################################################################################
    #
    #   VARIBLES
    #
    ##################################################################################################################################

    _message = None
    _reward = None
    _id = None

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self, id):
        self._id = id

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getMessage(self):
        return self._message

    # ----------------------------------------------------------------

    def getReward(self):
        return self._reward

    # ----------------------------------------------------------------

    def getID(self):
        return self._id

    # ----------------------------------------------------------------

    def hasMessageType(self):
        return False

    # ----------------------------------------------------------------

    def getJsonFormat(self):
        return {"message": self._message.getJsonFormat(), "name": self._id}

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    def setMessage(self, message):
        self._message = message

    # ----------------------------------------------------------------

    def setReward(self, value):
        self._reward = value

    ##################################################################################################################################
    #
    #   AUXILIAR
    #
    ##################################################################################################################################

    def __eq__(self, condition):
        return issubclass(type(condition), CONDITION) and self.getMessage() == condition.getMessage() and self.getID() == condition.getID()
