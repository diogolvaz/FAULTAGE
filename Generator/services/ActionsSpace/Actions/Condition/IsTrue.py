class TRUE(CONDITION):

    ##################################################################################################################################
    #
    #   VARIBLES
    #
    ##################################################################################################################################

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self, message):
        super().__init__("True")
        self.setMessage(message)
        self.setReward(0)

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getLabel(self):
        return " if true"

    # ----------------------------------------------------------------

    def isTrueCondition(self):
        return True

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    ##################################################################################################################################
    #
    #   AUXILIAR
    #
    ##################################################################################################################################

    def __eq__(self, condition):
        return super().__eq__(condition) and (type(condition) is TRUE)
