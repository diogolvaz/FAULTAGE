import copy

##################################################################################################################################
#
#   CLASS
#
##################################################################################################################################


class LOGIC:

    ##################################################################################################################################
    #
    #   VARIBLES
    #
    ##################################################################################################################################

    _name = None

    _label = None

    _message = None

    _reward = None

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    # Constructor
    def __init__(self, action_name, label, message, reward):
        self.setLabel(label)
        self.setName(action_name)
        self.setMessage(message)
        self.setReward(reward)

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getName(self):
        return self._name

    # ----------------------------------------------------------------

    def getLabel(self):
        return self._label

    # ----------------------------------------------------------------

    def getCopy(self):
        return copy.deepcopy(self)

    # ----------------------------------------------------------------

    def getExtraConditionLabel(self):
        return ""

    # ----------------------------------------------------------------

    def getNumberOfSends(self, N):
        return 0

    # ----------------------------------------------------------------

    def getMessage(self):
        return self._message

    # ----------------------------------------------------------------

    def getType(self):
        return self.getMessage().getType()

    # ----------------------------------------------------------------

    def getReward(self):
        return self._reward

    # ----------------------------------------------------------------

    def getJsonFormat(self):
        message = "None"
        if not self.getMessage() == None:
            message = self.getMessage().getJsonFormat()
        return {"name": self.getName(), "message": message}

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    def setName(self, name):
        self._name = name

    # ----------------------------------------------------------------

    def setLabel(self, label):
        self._label = label

    # ----------------------------------------------------------------

    def setMessage(self, message):
        self._message = message

    # ----------------------------------------------------------------

    def setReward(self, reward):
        self._reward = reward

    ##################################################################################################################################
    #
    #   VALIDATION
    #
    ##################################################################################################################################

    def isDELIVER(self):
        return False

    # ----------------------------------------------------------------

    def isSEND(self):
        return False

    # ----------------------------------------------------------------

    def isDECIDE(self):
        return False

    # ----------------------------------------------------------------

    def isPROPOSE(self):
        return False

    # ----------------------------------------------------------------

    def isSTOP(self):
        return False

    # ----------------------------------------------------------------

    def isFINISH(self):
        return False

    # ----------------------------------------------------------------

    def isSET_ESTIMATE(self):
        return False

    # ----------------------------------------------------------------

    def __eq__(self, logic):
        return issubclass(type(logic), LOGIC) and self.getMessage() == logic.getMessage()
