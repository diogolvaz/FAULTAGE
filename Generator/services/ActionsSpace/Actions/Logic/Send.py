##################################################################################################################################
#
#   CLASS
#
##################################################################################################################################


class SEND(LOGIC):

    ##################################################################################################################################
    #
    #   VARIABLES
    #
    ##################################################################################################################################

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self, action_name, message, label, reward):
        super().__init__(action_name, str(label)+" ("+message.getLabel()+")",
                         message, reward+message.getReward())
        super().setMessage(message)

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getExtraConditionLabel(self):
        return " and not already sent"

    ##################################################################################################################################
    #
    #   LOGIC
    #
    ##################################################################################################################################

    def isSEND(self):
        return True

    # ----------------------------------------------------------------

    def __eq__(self, logic):
        return super().__eq__(logic) and issubclass(type(logic), SEND)
