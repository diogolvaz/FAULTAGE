from services.ActionsSpace.Actions.Logic.Send import SEND
import inputs.GenerationInputs as GenerationInputs
import utils.GlobalVariables.TextVariables as Vars

##################################################################################################################################
#
#   CLASS
#
##################################################################################################################################


class SEND_N(SEND):

    ##################################################################################################################################
    #
    #   VARIABLES
    #
    ##################################################################################################################################

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self, message):
        super().__init__(Vars.SEND_NEIGHBORS, message, "SEND to neighbours",
                         GenerationInputs.getLogicReward(Vars.SEND_NEIGHBORS))

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getNumberOfSends(self, N):
        return N-1

    ##################################################################################################################################
    #
    #   LOGIC
    #
    ##################################################################################################################################

    def __eq__(self, logic):
        return super().__eq__(logic) and (type(logic) is SEND_N)
