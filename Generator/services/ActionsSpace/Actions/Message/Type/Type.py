import utils.GlobalVariables.TextVariables as TextVariables

##################################################################################################################################
#
#   CLASS
#
##################################################################################################################################


class TYPE():

    ##################################################################################################################################
    #
    #   VARIBLES
    #
    ##################################################################################################################################

    _value = None

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self, value):
        self._value = value

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getName(self):
        return TextVariables.TYPE

    # ----------------------------------------------------------------

    def getLabel(self):
        return self.getName()+str(self.getValue())

    # ----------------------------------------------------------------

    def getValue(self):
        return self._value

    # ----------------------------------------------------------------

    def getJsonFormat(self):
        return {"status": self.getName(), "value": self.getValue()}

    # ----------------------------------------------------------------

    def isType(self):
        return True

    # ----------------------------------------------------------------

    def isSuspectType(self):
        return False

    ##################################################################################################################################
    #
    #   AUXILIAR
    #
    ##################################################################################################################################

    def __eq__(self, type):
        return not type == None and self.getLabel() == type.getLabel()
