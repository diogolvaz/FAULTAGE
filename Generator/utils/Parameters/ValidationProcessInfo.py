import utils.Logger as Logger
from utils.Parameters.ProcessInfo import ProcessInfo

##################################################################################################################################
#
#   CLASS
#
##################################################################################################################################


class ValidationProcessInfo(ProcessInfo):

    ##################################################################################################################################
    #
    #   VARIABLES
    #
    ##################################################################################################################################

    _validation_result = None

    #_fullyValidated = None

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self):
        super().__init__()

    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getValidationResult(self):
        return self._validation_result

    # ----------------------------------------------------------------

    def isValid(self):
        if self._validation_result == None:
            return False
        else:
            return self._validation_result

    # ----------------------------------------------------------------

    def isFullyValidated(self):
        if self._validation_result == None:
            return False
        else:
            return True

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    def setValidationResult(self, value):
        if value == None:
            self._validation_result = None
        else:
            self._validation_result = bool(value)

    ##################################################################################################################################
    #
    #   AUXILIAR
    #
    ##################################################################################################################################

    def printValidationProcessInfo(self):
        Logger.logInfoMessage("=== Validation Process Info")
        Logger.logInfoMessage(
            "Validation Result: "+str(self.getValidationResult()))
        self.printProcessInfo()
        Logger.logInfoMessage("===\n")
