def exportTikzPictureData(mean, sd, filepath):
    counter = 1
    lines = []
    for index in range(len(mean)):
        lines += ["("+str(counter)+","+str(mean[index]) +
                  ")+-("+str(sd[index])+","+str(sd[index])+")\n"]
        counter += 1
    Graphics.writeTXTFile(lines, filepath)
