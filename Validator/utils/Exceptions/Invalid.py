class INVALID(Exception):

    ##################################################################################################################################
    #
    #   VARIABLES
    #
    ##################################################################################################################################
    
    _validation_inputs=None

    ##################################################################################################################################
    #
    #   CONSTRUCTOR
    #
    ##################################################################################################################################

    def __init__(self,validation_inputs):
        super().__init__()
        self.setValidationInputs(validation_inputs)

    ##################################################################################################################################
    #
    #   SET
    #
    ##################################################################################################################################

    def setValidationInputs(self, value):
        self._validation_inputs=value
    
    ##################################################################################################################################
    #
    #   GET
    #
    ##################################################################################################################################

    def getValidationInputs(self):
        return self._validation_inputs